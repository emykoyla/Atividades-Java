 
 //Area, raio e PI 

 public class Main
 {
   public static void main (String[]args)
   {
     final float VALOR_PI = 3.14f;
     double areaCirculo = 0;
     double raioCirculo = 89.76;
 
 
       areaCirculo = VALOR_PI * (raioCirculo * raioCirculo);
 
       System.out.println ("Resultado Area Circulo: " + areaCirculo);
   }
 }
 